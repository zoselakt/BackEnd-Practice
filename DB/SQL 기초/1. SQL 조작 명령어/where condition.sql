-- columnA between A and B: A와 B 사이의 값을 가져온다. 

-- or: 두값중 하나의 값이 참이면 해당하는 행을 가져온다.

-- Not: 조건에 해당하는 값이 아닌 행을 가져온다.

-- In: 조건값들을 포함한 행(row)들을 가져온다. 

-- Not in: 조건값들을 제외한 행(row)들을 검색한다.

-- Like: 조건에 해당하는 문자를 가진 행을 검색한다.

-- is null: NULL인 데이터 갖는 행(row)를 가져온다.

